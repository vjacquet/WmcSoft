using System;

namespace WmcSoft
{
    /// <summary>
    /// Represents the lower and upper limits of an interval.
    /// </summary>
    /// <typeparam name="T">The type of element for which we define an interval.</typeparam>
    public interface IInterval<T>
        where T : struct, IComparable<T>
    {
        T? Lower { get; }
        T? Upper { get; }

        bool IsLowerIncluded { get; }
        bool IsUpperIncluded { get; }
    }
}
